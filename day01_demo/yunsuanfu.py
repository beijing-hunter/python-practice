#格式化代码快捷键 Ctrl + Alt + L

# 指数运算
value = 2 ** 3
print(value)

# 整除运算
print(13 // 2)

# 取余数运算
print(13 % 2)

# 赋值
num1, num2 = 12, "tom"
print(f"num1={num1},num2={num2}")

# 符合赋值
c = 10
c += 12
print(f"c={c}")

b = 12
b += 1 + 12
print(f"b={b}")

# 比较运算符
print((1 == 1 and 1 == 2))

a = 3
b = 0
print(a and b)
print(a or b)
